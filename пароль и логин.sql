CREATE TABLE Пользователи (
    id_пользователя INT PRIMARY KEY IDENTITY(1,1),
    логин NVARCHAR(50) NOT NULL UNIQUE,
    пароль NVARCHAR(100) NOT NULL,
    ФИО NVARCHAR(100) NOT NULL,
    должность NVARCHAR(50) NOT NULL,
    роль NVARCHAR(20) NOT NULL CHECK (роль IN ('Администратор', 'Директор', 'Бухгалтер', 'Сотрудник')),
    дата_регистрации DATETIME DEFAULT GETDATE(),
    активен BIT DEFAULT 1
);

